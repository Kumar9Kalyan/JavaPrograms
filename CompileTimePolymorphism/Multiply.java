package programs.leetcode;

public class Multiply {

	//Method overloading by different type of arguments
	
		static int mul(int n1, int n2) {
			return n1*n2;
		}
		
		static double mul(double n1,double n2) {
			return n1*n2;
		}
		
		static float mul(float n1,float n2) {
			return   (n1*n2);
		}
		

}
